n1 = [0,1,2,3,4];
x1 = [1,1,1,1,1];
n2 = [0,1,2,3,4,5,6];
h2 = [2^0,2^1,2^2,2^3,2^4,2^5,2^6];
y = conv(x1,h2);
n = 0:10;
 
subplot(2,2,1);
stem(n1,x1);
title('x[n] = 1, 0<=n<=4');
subplot(2,2,2);
stem(n2,h2);
title('h[n] = 2^n, 0<=n<=6');
subplot(2,2,3);
stem(n,y);
title('y[n] = x[n] * h[n]');
xlabel('<-----------n--------->');
ylabel('<-----------y[n]--------->');